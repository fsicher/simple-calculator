package simple.calculator;

public class Calculator {
	
	/**
	 * Calculates the sum of a and b.
	 * @param a
	 * @param b
	 * @return
	 */
	public static double add(double a, double b) {
		return a+b;
	}
	
	/**
	 * Calculates the difference of a and b.
	 * @param a
	 * @param b
	 * @return
	 */
	public static double sub(double a, double b) {
		return a-b;
	}
}
